#!/bin/bash

installed_dir=$(dirname "$(readlink -f "$(basename "$(pwd)")")")

########################################################################################################
# Debug Mode
########################################################################################################
if [ "$DEBUG" = true ]; then
    echo
    echo "------------------------------------------------------------"
    echo "Running $(basename "$0")"
    echo "------------------------------------------------------------"
    echo
    read -n 1 -s -r -p "Debug mode is on. Press any key to continue..."
    echo
fi

########################################################################################################
# AUR Software Installation Header
########################################################################################################
echo
tput setaf 2
echo "########################################################################"
echo "################### AUR from folder - Software to install"
echo "########################################################################"
tput sgr0
echo

result=$(systemd-detect-virt)

if [ "$result" = "none" ]; then
    echo
    tput setaf 2
    echo "########################################################################"
    echo "####### Installing VirtualBox"
    echo "########################################################################"
    tput sgr0
    echo

    sh AUR/install-virtualbox-for-linux.sh
else
    echo
    tput setaf 3
    echo "########################################################################"
    echo "### You are on a virtual machine - skipping VirtualBox"
    echo "########################################################################"
    tput sgr0
    echo
fi

########################################################################################################
# No AUR Packages to Build
########################################################################################################
echo
tput setaf 2
echo "########################################################################"
echo "################### No additional AUR packages to install"
echo "########################################################################"
tput sgr0
echo

########################################################################################################
# Done
########################################################################################################
echo
tput setaf 6
echo "##############################################################"
echo "###################  $(basename "$0") done"
echo "##############################################################"
tput sgr0
echo

